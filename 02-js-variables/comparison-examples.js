// Provides a set of examples using the JavaScript Comparison Operators
var x = 10, y = 20;
console.log(x == y);
console.log(x == 10);
console.log(x == '10'); // JavaScript converts the string to a number
console.log(x === 10);
console.log(x === '10');
console.log(x != y);
console.log(x !== y);
console.log(x > y);
console.log(x >= y);
console.log(x < y);
console.log(x <= y);
console.log(x > y ? 'greater' : 'lessthan');
